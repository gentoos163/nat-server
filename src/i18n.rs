/// 从请求头解析语言，返回 "zh" 或 "en"
pub fn detect(lang_cookie: Option<&str>, accept_lang: Option<&str>) -> &'static str {
    if let Some(c) = lang_cookie {
        if c == "en" {
            return "en";
        }
        if c == "zh" {
            return "zh";
        }
    }
    if let Some(al) = accept_lang {
        if al.contains("zh") {
            return "zh";
        }
    }
    "zh" // 默认中文
}

pub fn get(lang: &str) -> T {
    if lang == "en" {
        en()
    } else {
        zh()
    }
}
